매니저존재여부는 각 사원에 대해 

NVL은 
    관리자가 있으면 manager_id와 관리자가 없으면 '0'으로 표시,
    관리자가 있으면 manager_id와 관리자가 없으면 '없음'으로 표시,
NVL2는
사원의 관리자(매니저)가 있으면 '있음' 으로 --manager_id, 'O','Manager'   
       관리자(매니저)가 없으면 '없음' 으로 --'없음',     'X','No Manager' 등으로 조회되게 한다.
       
