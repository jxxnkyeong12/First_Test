2. NVL2
사번 성      부서코드 업무코드  입사일자 manager_id 없으면0(NVL2) 없으면없음(NVL2) 매니저있음없음(NVL2) manager_id 없음(NVL2) 있으면O없으면X(NVL2) Manager No Manager(NVL2)

SELECT  employee_id "사번", last_name "성", department_id "부서코드", job_id "업무코드", hire_date "입사일자", manager_id "manager_id",
        NVL2(manager_id, manager_id, 0) "manager_id/0(NVL)",
        NVL2(manager_id, TO_CHAR(manager_id), '없음') "manager_id/없음(NVL)",
        NVL2(manager_id, '있음','없음') "있음/없음(NVL)",   
        NVL2(manager_id, 'O','X') "O/X(NVL)",                    
        NVL2(manager_id, 'Manager', 'No Manager')  "Manager/No Manager(NVL)"      
FROM    employees;
